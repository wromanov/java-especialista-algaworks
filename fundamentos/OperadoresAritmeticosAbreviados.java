package fundamentos;

public class OperadoresAritmeticosAbreviados {
    public static void main(String[] args) {

        //Declarando variável;
        int number = 10;
        int number2 = 10;
        int number3 = 10;

        //Incrementado o valor de number em mais 1.
        number = number + 1;

        //Abreviando a declaração em mais 1.
        number += 1;

        System.out.println(number);

        //subtração
        number2 -= 1;
        System.out.println(number2);

        //Multiplicação
        number3 *= 1;
        System.out.println(number3);

    }
}
