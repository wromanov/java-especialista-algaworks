package fundamentos;
public class Variaveis {
    public static void main(String[] args) {

        //Declarando Variáveis
        int numeroInteiro;
        double numeroFlutuante;
        String cadeiaCaracteres;

        //Usamos LowerCamelCase para Criar Variáveis
        numeroInteiro = 123456;
        numeroFlutuante = 1234567.89;
        cadeiaCaracteres = "Texto Escrito Aqui.";

        System.out.println("Numero Inteiro: " + numeroInteiro);
        System.out.println("Numero Flutuante: " + numeroFlutuante);
        System.out.println("Cadeia Caracteres: " + cadeiaCaracteres);
    }
}
