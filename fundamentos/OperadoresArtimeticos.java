package fundamentos;

public class OperadoresArtimeticos {

    public static void main(String[] args) {

        //Operadores Aritméticos JAVA

        int number1 = 5;
        int number2 = 3;

        //Adição
        int adicao = number1 + number2;
        System.out.println("Resultado da Adição: " + adicao);

        //Subtração
        int subtracao = number1 - number2;
        System.out.println("Resultado da Subtração: " + subtracao);

        // Multiplicação
        int multiplicacao = number1 * number2;
        System.out.println("Resultado da Multiplicação: " + multiplicacao);

        //Divisao Inteira
        int divisao = number1 / number2;
        System.out.println("Resultado da Divisão: " + divisao);

        //Modulo da Divisão (resto)
        int moduloDivisao = number1 % number2;
        System.out.println("Modulo da Divisão: " + moduloDivisao);


    }


}
